        <div class="panel panel-default">
            <div id="perg_2" class="panel-heading">
                <h4>Como consigo dinheiro para um barco?</h4>
            </div>
            <div class="panel-body">
                A forma inicial de conseguir dinheiro para um barco é indo ao subúrbio da ilha se for um pirata ou indo
                até a base da marinha da ilha se você for um marinheiro, lá tem missões que ao serem concluidas te
                recompensam com experiência e dinheiro.
            </div>
        </div>

        <div class="panel panel-default">
            <div id="perg_3" class="panel-heading">
                <h4>Não consigo iniciar uma missão. O que eu faço?</h4>
            </div>
            <div class="panel-body">
                Verifique se você cumpre todos os requisitos para as missões lendo atentamente o que é necessário para
                inicia-la.
            </div>
        </div>

        <div class="panel panel-default">
            <div id="perg_4" class="panel-heading">
                <h4>Como subo de nível?</h4>
            </div>
            <div class="panel-body">
                Na barra da tripulação, onde aparecem os cartazes de procurado, aparece uma Espada Amarela na frente dos
                tripulantes que já podem evoluir, você pode clicar na espada ou ir até a Visão Geral da tripulação
                através do menu do jogo. Fique de olho na tripulação, todos seus tripulantes tem sua própria experiência
                individual.
            </div>
        </div>

        <div class="panel panel-default">
            <div id="perg_5" class="panel-heading">
                <h4>Como compro um navio?</h4>
            </div>
            <div class="panel-body">
                Você encontrará barcos a venda no Estaleiro de cada Ilha.
            </div>
        </div>

        <div class="panel panel-default">
            <div id="perg_6" class="panel-heading">
                <h4>Como recruto mais tripulantes?</h4>
            </div>
            <div class="panel-body">
                Na Ilha Atual, existe uma opção chamada "Recrutar", lá você irá iniciar uma campanha em busca de novos
                tripulantes na ilha, quando o tempo acabar você poderá escolher um novo tripulante, dar um nome para
                ele, então ele irá fazer parte da sua tripulação.
            </div>
        </div>

        <div class="panel panel-default">
            <div id="perg_7" class="panel-heading">
                <h4>Como navego pelo oceano?</h4>
            </div>
            <div class="panel-body">
                Na menu você encontrará uma opção "Ir para o oceano", lá você deve traçar uma rota de navegação, dê dois
                cliques nos quadros ao redor do seu navio para ir criando sua rota. Depois de criar a rota basta clicar
                em "Navegar" e acompanhar seu barco nevegando até chegar ao seu destino.
            </div>
        </div>

        <div class="panel panel-default">
            <div id="perg_13" class="panel-heading">
                <h4>Como enfrento outros jogadores?</h4>
            </div>
            <div class="panel-body">
                <p>
                    Para atacar outros jogadores no oceano você tem que cumprir alguns requisitos:
                </p>
                <ul>
                    <li>
                        Ter seu tripulante de nível mais alto no nível 10
                    </li>
                    <li>
                        Piratas atacam a todos enquanto marinheiros só atacam piratas
                    </li>
                    <li>
                        Nem você nem seu alvo podem estar ao redor de ilhas. Em volta de cada ilha é uma zona neutra,
                        nao da pra atacar ninguém la.
                    </li>
                    <li>
                        Os tripulantes de nível mais alto de cada tripulação devem estar na mesma faixa de nível, por
                        exemplo, nos Blues (North Blue, East Blue, South Blue e West Blue) a faixa de nível é de 5
                        níveis de diferença, então se seu personage de nível mais alto estiver no nível 15, você só pode
                        atacar jogadores de nível do mais forte entre 10 e 20. Na Grand Line a faixa de nível é de 10
                        níveis, e no Novo Mundo Não existe faixa de nível.
                    </li>
                    <li>
                        Se você fizer parte de uma aliança ou frota, você não pode atacar jogadores que também
                        participam da sua aliança ou frota
                    </li>
                    <li>
                        Se você fizer parte de uma aliança ou frota e ela entrar em guerra contra outra aliança ou
                        frota, todas as restrições acima listadas são ignoradas para os jogadores membros da aliança ou
                        frota inimiga de guerra, e vocês podem se atacar a qualquer momento em qualquer lugar.
                    </li>
                </ul>
            </div>
        </div>

        <div class="panel panel-default">
            <div id="perg_8" class="panel-heading">
                <h4>Entrei em uma batalha contra um Rei dos Mares ou outro jogador. que eu faço?</h4>
            </div>
            <div class="panel-body">
                As batalhas são baseadas em turnos, um jogador de cada vez movimentando apenas 1 personagem. Primeiro
                escolha se você irá atacar ou mover seus personagens, cada jogador pode fazer até 5 movimentos na sua
                vez. Se escolher atacar, você deve selecionar o personagem que irá atacar, em seguida escolher a
                habilidade e depois escolher nde ele irá atacar. Se você quiser acertar um Rei dos Mares, você precisa
                subir até a última linha do tabuleiro e ficar bem próximo a ele para acertar o ataque.
            </div>
        </div>

