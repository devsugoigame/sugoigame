            <div id="perg_1" class="panel-heading">
                <h4>Acabei de me cadastrar, o que faço agora?</h4>
            </div>
            <div class="panel-body">
                Seu primeiro objetivo é comprar um barco para poder recrutar mais tripulantes e poder navegar por aí.
            </div>
